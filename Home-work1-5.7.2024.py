num1: int = int(input("Insert 1st integer number: "));
num2: int = int(input("Insert 2nd integer number: "));
num3: int = int(input("Insert 3rd integer number: "));

sum_result = num1 + num2 + num3;
multi_result = num1 * num2 * num3;

print(num1, '+', num2, '+', num3, '=', sum_result);
print(num1, '*', num2, '*', num3, '=', multi_result);
